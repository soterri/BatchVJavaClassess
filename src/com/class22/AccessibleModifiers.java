package com.class22;

public class AccessibleModifiers {
	
	public static void hello() {
		System.out.println("Hello");
	}
	protected static void hello1() {
		System.out.println("Hello1");
	}
	static void hello2() {
		System.out.println("hello2");
	}
	private static void hello3() {
		System.out.println("hello2");
	}
}
