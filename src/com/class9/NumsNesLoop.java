package com.class9;

public class NumsNesLoop {

	public static void main(String[] args) {
		
		
		
		//to print 123456789 5 rows 9 columns
		for (int a=0; a<5; a++) {
		for (int b=1; b<10; b++) {
			System.out.print(b);
		}
		System.out.println();
		}
		
		for (int c=5; c>5; c--) {
		for (int d=5; d>=1; d--) {
			System.out.print(d);
	}
		System.out.println();
	}
		for (int e=5; e>0; e--) {
		for (int f=5; f>0; f--) {
			System.out.println(e);
		}
		System.out.println();
		}
}
}
