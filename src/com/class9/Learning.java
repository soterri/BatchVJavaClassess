package com.class9;

public class Learning {

	public static void main(String[] args) {
		
		
		//PYRAMID LOOP
		//*
		//**
		//***
		//****
		//*****
		
		for (int i=1; i<=5; i++) {
		
		for (int j=1; j<=i; j++) {
			
		System.out.print("*");
		}
		System.out.println();
		}
		//1
		//22
		//333 and so on 4444 55555 so on until 999999999
		
		for (int i=1; i<=9; i++) {
		for (int j=1; j<=i; j++) {
			System.out.print(i);
		}
		System.out.println();
		}
		for (int i=9; i>=1; i--) {
		for (int j=1; j<=i; j++) {
			System.out.print(i);
		}
		System.out.println();
		
			
		}
		}
		
	}

